                                    <p>
                                       Preparar... Apontar... Vai!
                                    </p><p>
                                        O ambiente corporativo possui uma relação próxima com os esportes. Aspectos como liderança, trabalho em equipe e superação são constantes em ambos. Porém, nem sempre essas semelhanças são aproveitadas como poderiam.
                                    </p><p>
                                        Uma das principais característica dos esportes é o conceito de "jogo". Nele, existem duas figuras essenciais, o jogador e as regras. As regras são desafios inventados, adotados por opção. Um exemplo disso são filhotes de leão brincando entre si. Esse exercício não é essencial para suas vidas de modo direto, mas sim indiretamente, visto que aprimorar suas habilidades motoras pode significar escapar da morte no futuro. Felizmente, nossos desafios como seres humanos não representam risco de morte, mas nem por isso são levados menos a sério.
                                    </p><p>
                                        Assim sendo, esse conceito de "desafio adotado" também é aplicado por nós, com impactos semelhantes, indiretos. É nesse ponto em que entram os esportes. Ao definirmos jogadores, equipes e regras, estamos recriando em um ambiente controlado muitas das relações vistas nas atividades do dia-a-dia. Nesse ambiente é possível escolher, medir e evoluir características essenciais para um bom ambiente corporativo e de convivência.
                                    </p><p>
                                        Assim, a aplicação consciente dos esportes pode ser uma maneira eficaz de aprimorar relações interpessoais, dentro e fora de campo. Agora, pra de fato marcar esse gol, vamos precisar de ajuda.
                                    </p><p>
